package mvc.modele.service;

/**
 * Enumération représentant les deux types de services de restauration disponibles.
 *
 * RESTAURANT : service avec réservation possible, généralement à table, avec une capacité plus grande.
 * CAFETERIA  : service en libre-service (souvent self), généralement sans réservation, avec des prix plus bas.
 */
public enum TypeService {
	RESTAURANT, CAFETERIA
}
